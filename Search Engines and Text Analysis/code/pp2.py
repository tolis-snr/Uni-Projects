nltk.download('stopwords')

# Αρχικοποίηση εργαλείων
stop_words = set(stopwords.words('english'))
stemmer = PorterStemmer()

def clean_text(text):
    """
    Δέχεται ένα κείμενο (string) και επιστρέφει το καθαρισμένο κείμενο.
    Βήματα:
    1. Αφαίρεση HTML tags (αν υπάρχουν) και ειδικών χαρακτήρων.
    2. Μετατροπή σε πεζά (lowercase).
    3. Αφαίρεση σημείων στίξης και αριθμών.
    4. Αφαίρεση Stopwords.
    5. Stemming (μείωση λέξεων στη ρίζα τους).
    """
    # 1. Αφαίρεση μη αλφαβητικών χαρακτήρων (κρατάμε μόνο γράμματα)
    text = re.sub(r'[^a-zA-Z]', ' ', text)

    # 2. Μετατροπή σε πεζά
    text = text.lower()

    # 3. Tokenization (χωρισμός σε λίστα λέξεων)
    words = text.split()

    # 4 & 5. Αφαίρεση Stopwords και Stemming
    # Κρατάμε τη λέξη αν ΔΕΝ είναι στα stopwords, και της κάνουμε stem
    cleaned_words = [stemmer.stem(word) for word in words if word not in stop_words]

    # Ενωση ξανά σε string
    return ' '.join(cleaned_words)

