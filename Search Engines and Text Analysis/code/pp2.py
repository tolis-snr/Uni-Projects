# Συνάρτηση που τα συνδυάζει όλα (Σενάριο 5: Baseline + Όλα μαζί)
def clean_text_all(text):
    # 1. Βάρη Θέσης (Position)
    step1 = clean_text_with_position(text)
    # 2. Βάρη POS (Part of Speech)
    step2 = clean_text_with_pos(step1)
    # 3. Διαχείριση Άρνησης (Negation)
    step3 = clean_text_with_negation(step2)
    return step3

