import nltk
from nltk import pos_tag
from nltk.tokenize import word_tokenize

# Κατεβάζουμε τα απαραίτητα αρχεία για να ξέρει το NLTK τι είναι ρήμα, επίθετο κτλ.
nltk.download('averaged_perceptron_tagger_eng')
nltk.download('punkt_tab') # Χρειάζεται για το tokenization

def clean_text_with_pos(text):
    # 1. Tokenization: Χωρίζουμε το κείμενο σε λέξεις
    words = word_tokenize(text)

    # 2. POS Tagging: Βρίσκουμε τι μέρος του λόγου είναι κάθε λέξη
    # Επιστρέφει ζευγάρια: [('movie', 'NN'), ('is', 'VBZ'), ('good', 'JJ')]
    tagged_words = pos_tag(words)

    final_tokens = []

    # Λίστα με ετικέτες που μας ενδιαφέρουν (JJ=Adjective, RB=Adverb)
    # JJ, JJR, JJS = Επίθετα (απλό, συγκριτικό, υπερθετικό)
    # RB, RBR, RBS = Επιρρήματα
    target_tags = {'JJ', 'JJR', 'JJS', 'RB', 'RBR', 'RBS'}

    for word, tag in tagged_words:
        # Καθαρίζουμε λίγο τη λέξη (lowercase)
        clean_word = word.lower()

        # Αν είναι γράμμα (όχι σημείο στίξης)
        if clean_word.isalpha():
            final_tokens.append(clean_word) # Προσθέτουμε τη λέξη μία φορά (βάρος 1)

            # Αν είναι Επίθετο ή Επίρρημα, την ξαναπροσθέτουμε (βάρος 1+1=2)
            if tag in target_tags:
                final_tokens.append(clean_word)

    return " ".join(final_tokens)

