from sklearn.base import BaseEstimator, TransformerMixin
from gensim.models import Word2Vec
from sklearn.preprocessing import MinMaxScaler

class Word2VecVectorizer(BaseEstimator, TransformerMixin):
    def __init__(self, vector_size=100, window=5, min_count=1):
        self.vector_size = vector_size
        self.window = window
        self.min_count = min_count
        self.model = None
        self.scaler = MinMaxScaler() # Όπως ζητάει η εκφώνηση (0-1 scaling)

    def fit(self, X, y=None):
        # Το Word2Vec θέλει λίστα από λίστες λέξεων (sentences)
        sentences = [text.split() for text in X]
        # Εκπαίδευση του μοντέλου
        self.model = Word2Vec(sentences, vector_size=self.vector_size,
                              window=self.window, min_count=self.min_count, workers=4)
        return self

    def transform(self, X):
        # Mean Pooling: Μέσος όρος των διανυσμάτων των λέξεων για κάθε κείμενο
        words_lists = [text.split() for text in X]
        vectors = []
        for words in words_lists:
            word_vectors = [self.model.wv[w] for w in words if w in self.model.wv]
            if len(word_vectors) > 0:
                # Μέσος όρος (axis=0)
                vectors.append(np.mean(word_vectors, axis=0))
            else:
                # Αν δεν βρεθούν λέξεις (σπάνιο), βάζουμε μηδενικό διάνυσμα
                vectors.append(np.zeros(self.vector_size))

        # Μετατροπή σε numpy array
        vectors = np.array(vectors)

        # Scaling στο 0-1 (Απαραίτητο για Naive Bayes)
        return self.scaler.fit_transform(vectors)

