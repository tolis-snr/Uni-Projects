# Υλοποίηση Στάθμισης Θέσης (Position Weighting)
from nltk.tokenize import sent_tokenize

nltk.download('punkt_tab')

def clean_text_with_position(text):
    # 1. Χωρίζουμε το κείμενο σε προτάσεις
    sentences = sent_tokenize(text)

    if not sentences:
        return ""

    # Ορίζουμε ποιες προτάσεις είναι σημαντικές (1η και Τελευταία)
    first_sentence = sentences[0]
    last_sentence = sentences[-1] if len(sentences) > 1 else ""

    # 2. Φτιάχνουμε το τελικό κείμενο:
    # Αρχικό κείμενο (όλα με βάρος 1) + 1η Πρόταση (ξανά) + Τελευταία Πρόταση (ξανά)
    augmented_text = text + " " + first_sentence + " " + last_sentence

    # 3. Κάνουμε έναν απλό καθαρισμό (lowercase κτλ) στο τελικό αποτέλεσμα
    # Χρησιμοποιούμε regex για να κρατήσουμε μόνο λέξεις
    final_tokens = re.findall(r'\w+', augmented_text.lower())

    return " ".join(final_tokens)

# --- Δοκιμή ---
