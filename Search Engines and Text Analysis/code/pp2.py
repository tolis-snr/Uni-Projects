import re

def clean_text_with_negation(text):
    # 1. Αρχικά κάνουμε τα βασικά (lowercase)
    text = text.lower()

    # 2. Χωρίζουμε το κείμενο σε λέξεις (tokens), κρατώντας και τα σημεία στίξης
    # Αυτό το regex χωρίζει λέξεις και κρατάει τα σημεία στίξης ξεχωριστά
    tokens = re.findall(r"[\w']+|[.,!?;]", text)

    # Λίστα με λέξεις που ξεκινάνε την άρνηση
    negation_triggers = {"not", "no", "never", "n't", "cannot", "nothing", "nowhere"}

    final_tokens = []
    negation_flag = False

    for word in tokens:
        if word in [".", ",", "!", "?", ";"]:
            negation_flag = False
            continue # Δεν κρατάμε το σημείο στίξης (προαιρετικό)


        if negation_flag:
            word = "NOT_" + word


        # (Ελέγχουμε αν η λέξη είναι trigger ή αν τελειώνει σε n't π.χ. don't)
        if word in negation_triggers or word.endswith("n't"):
            negation_flag = True

        # Προσθέτουμε τη λέξη (αλλαγμένη ή όχι) στη λίστα
        final_tokens.append(word)

    return " ".join(final_tokens)

